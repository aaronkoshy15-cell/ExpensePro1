let currentLanguage = localStorage.getItem('appLanguage') || 'en';

window.t = function(englishText) {
    if (currentLanguage === 'en') return englishText;
    const langDict = i18nDictionary[currentLanguage];
    if (langDict && langDict[englishText]) {
        return langDict[englishText];
    }
    return englishText;
};

function translateDOM(node) {
    if (node.nodeType === Node.TEXT_NODE) {
        const text = node.nodeValue.trim();
        if (text) {
            const translated = window.t(text);
            if (translated !== text) {
                // To avoid trimming leading/trailing spaces, preserve them
                node.nodeValue = node.nodeValue.replace(text, translated);
            }
        }
    } else if (node.nodeType === Node.ELEMENT_NODE) {
        // Skip script and style tags
        if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') return;
        
        // Translate placeholders
        if (node.hasAttribute('placeholder')) {
            const placeholder = node.getAttribute('placeholder');
            const translated = window.t(placeholder);
            if (translated !== placeholder) node.setAttribute('placeholder', translated);
        }

        // Translate specific custom elements or attributes if needed

        Array.from(node.childNodes).forEach(translateDOM);
    }
}

window.changeLanguage = function(lang) {
    currentLanguage = lang;
    localStorage.setItem('appLanguage', lang);
    location.reload(); 
};

// Apply language on load
document.addEventListener('DOMContentLoaded', () => {
    // Set selector value if it exists
    const langSelect = document.getElementById('languageSelect');
    if (langSelect) langSelect.value = currentLanguage;
    
    if (currentLanguage !== 'en') {
        translateDOM(document.body);
    }
});

// A MutationObserver could watch for new dynamically added text (like dynamic transactions) and translate them
const observer = new MutationObserver((mutations) => {
    if (currentLanguage === 'en') return;
    
    mutations.forEach((mutation) => {
        if (mutation.type === 'childList') {
            mutation.addedNodes.forEach((node) => {
                translateDOM(node);
            });
        }
    });
});

document.addEventListener('DOMContentLoaded', () => {
    observer.observe(document.body, { childList: true, subtree: true });
});
